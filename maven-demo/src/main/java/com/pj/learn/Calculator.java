package com.pj.learn;

/**
 * This {@code Calculator} class implements some simple calculator methods.
 * @since  3/24/2023
 * @author PJ Mwangi
 */
public class Calculator {
	
	/**
	 * Calculates the square of a number(x) passed as a parameter.
	 * <pre> Formula: square(x) = x * x </pre>
	 * @param x number to square
	 * @return  the square of the parameter x
	 */
	public double square(double x) {
		return x * x;
	}
}
